package com.walgreens.bigdata.demo.service.controller;

public class Record {

	private String date;
	private String distance;
	private String townName;
	private String townName2;
	private String townSize;
	private String latitude;
	private String duration;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getDistance() {
		return distance;
	}
	public void setDistance(String distance) {
		this.distance = distance;
	}
	public String getTownName() {
		return townName;
	}
	public void setTownName(String townName) {
		this.townName = townName;
	}
	public String getTownName2() {
		return townName2;
	}
	public void setTownName2(String townName2) {
		this.townName2 = townName2;
	}
	public String getTownSize() {
		return townSize;
	}
	public void setTownSize(String townSize) {
		this.townSize = townSize;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getDuration() {
		return duration;
	}
	public void setDuration(String duration) {
		this.duration = duration;
	}
	
	

}
